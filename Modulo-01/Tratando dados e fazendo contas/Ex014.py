"""Exercício Python 14: Escreva um programa que converta uma temperatura digitando em graus Celsius e converta para graus Fahrenheit."""

grua_celsius = float(input('Qual a temperatura em °C: '))

fahrenheit = (grua_celsius * 1.8) + 32

print(f'A temperatura de {grua_celsius}°C corresponde a {fahrenheit}°F!')
